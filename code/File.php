function add(int $a, int $b): int
{
    return $a + $b;
}


function substract(int $a, int $b): int
{
    return $a - $b;
}


function divide(int $a, int $b): int
{
    return $a / $b;
}


function multiply(int $a, int $b): int
{
    return $a * $b;
}

